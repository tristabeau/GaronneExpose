		<p>Tout le monde n'est cependant pas auteur quand bien même ils ont des idées bouillantes, et l'essentiel reste de fournir un contenu de qualité, tant dans son fond que dans sa forme. Si vous ne vous sentez pas l'âme d'un rédacteur, vous pouvez également nous proposer un sujet à traiter — sans garantie qu'il le sera, au demeurant, nous ne prenons pas commande !</p>
		</div>
		
		<div class="header clearfix"><h4>Que ne pas publier ?</h4></div>
        
		<div class="hyphenate text">
        <p>
		Les copies de dossiers de presse, et les copié-collés issus d'ailleurs et dont vous n'êtes pas l'auteur sont à éviter. Si vous n'êtes pas auteur de votre texte, il n'y a aucune valeur ajoutée à votre publication ; de plus, un texte recopié sans accord peut s'avérer périlleux au niveau légal, compte tenu des lois de propriété intellectuelle (qui peuvent faire l'objet d'un article ou d'une chronique, par ailleurs !)<br>
		De même, nous éviterons la publication d'articles dans d'autres conditions telles que (mais non limitées à) :
		</p>
			<ul>
				<li>les propos non constructifs (« trolls ») n'ayant pas de portée comique ou polémique.</li>
				<li>les publicités cachées et les placements de produit — Vous pouvez être entiché d'un sujet et ne pas être objectif, mais il faut que cela reste innocent !</li>
				<li>les publications n'ayant aucun intérêt littéraire, artistique ou informatif.</li>
			</ul>
		</div>
    </section>

    <section class="widget col-md-6">

        <div class="header clearfix"><h4>Quand publier ?</h4></div>

		<div class="hyphenate text">
        <p>Quand vous vient l'envie ! Cependant, pour des raisons éditoriales, vos textes ne seront peut-être pas affichés de suite. En effet, nous considérons que l'accumulation de news concentrées sur une courte période de temps n'incite pas à une démarche qualitative ; de plus, les internautes n'ont pas pour habitude de revenir lire des articles de fond plusieurs fois dans la même journée. Prenant en compte ces deux phénomènes, nous serons éventuellement amené à patienter quelques heures ou jours avant de publier vos écrits, sans que cela ne remette en cause la qualité de ceux-ci.
        </p>
		</div>
		
		<div class="header clearfix"><h4>Comment publier ?</h4></div>

		<div class="hyphenate text">
        <p>Merci de respecter les règles d'usage de la grammaire, de l'orthographe, et de la typographie. J'insiste sur ce dernier point majoritairement dédaigné des écrits numériques. Il arrive à tout le monde de faire des fautes, mais il est essentiel de limiter celles-ci. Un correcteur relira votre publication, mais nous parlons ici d'un travail d'édition, et non pas d'autorat. Pour éviter la majeure partie des fautes typographiques et orthographiques usuelles, vous pouvez utiliser votre logiciel de traitement de texte préféré avant de copier-coller le résultat dans la fenêtre du site.<br>
		Vous pouvez utiliser également les ressources suivantes pour vous y retrouver « à la main » :
        </p>
			<ul>
				<li>les <a href="http://jacques-andre.fr/faqtypo/lessons.pdf"><em>Petites leçons de typographie</em></a>, par Jacques André</li>
				<li>les <em>Règles de typographie française</em> de <a href="http://www.framasoft.net/article2225.html">Framasoft</a></li>
				<li>le site web de <a href="http://www.lanthologiste.fr/memo-typo/">l'anthologiste</a> (qui a d'excellents conseils d'écriture de manière générale)</li>
				<li>le conjugueur de <a href="http://www.bescherelle.com/conjugueur.php">Bescherelle</a> ou celui du <a href="http://leconjugueur.lefigaro.fr/">Figaro</a></li>
				<li>et enfin un précis de grammaire courante de <a href="http://www.synapse-fr.com/grammaire/GTM_0.htm">Synapse et Cordial</a></li>
			</ul>
		<p>
		Vous devez aussi joindre au moins une image pour mettre en avant votre publication. Assurez-vous que celle-ci soit bien libre de droit, ou liée à une <a href="http://search.creativecommons.org/?lang=fr">licence permissive</a> avant toute utilisation !
		</p>
		</div>

